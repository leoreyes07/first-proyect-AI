export const positionGroups: Record<PositionGroup, Position[]> = {
  Goalkeepers: ['GK'],
  Defenders: ['DEF'],
  Midfielders: ['MID'],
  Forwards: ['FWD'],
}

export function getPlayerById(id: string): Player | undefined {
  return players.find((player) => player.id === id)
}

export function getPlayersByPosition(position: Position): Player[] {
  return players.filter((player) => player.position === position)
}

export function getPlayersByGroup(group: PositionGroup): Player[] {
  return players.filter((player) => positionGroups[group].includes(player.position))
}

export function getSquadNumbers(): Map<number, Player> {
  const map = new Map<number, Player>()
  players.forEach((player) => map.set(player.number, player))
  return map
}
